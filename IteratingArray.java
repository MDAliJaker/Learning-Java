public class IteratingArray {
       
    public static void main(String[] args) {
        String[] country= {"Bangladesh", "Pakistan", "Malaysia", "Indonesia", "Maldip","Palestine"};
        for(String i : country){
            System.out.println(i+" is a Muslim country, Allhamdulillah. ");
        }

        int[] num = {13,34,13,44,24,2,4,25,2,54,243};
        for(int a: num){
            System.out.println(a);
        }
    }
}
