public class for_each_loop {
    public static void main(String[] args) {
        String[] cars = { "Buggati", "BMW", "Ford", "Toyota", "Mitsubi" };

        // for each loop
        for (String i : cars) {
            System.out.println(i);
        }

        int[] number = { 1, 2, 3, 4, 5, 6, 9, 10 };
        for (int i : number) {
            System.out.println(i);
        }
    }
}
